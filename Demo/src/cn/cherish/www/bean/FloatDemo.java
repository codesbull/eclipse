package cn.cherish.www.bean;
public class FloatDemo {
	public static void main(String[] args) {
		
		//float类型的占4字节  float类型有数的精度和指数
		System.out.println("flaot类型的最大值："+Float.MAX_VALUE);
		System.out.println("int类型的最大值："+Integer.MAX_VALUE);
		System.out.println("double类型的最大值："+Double.MAX_VALUE);
		char c='A';
		int a=65;
		System.out.println(c);
		System.out.println((char)a);
		//强制类型转换 int到字符型  (数据类型)要转换的变量
		boolean aa=false;
		System.out.println(aa);
	}

}
